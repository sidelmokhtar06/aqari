const LANG_KEY = 'aqari_lang';

export function getLang() {
  return localStorage.getItem(LANG_KEY) || 'fr';
}

export function t(key, lang = getLang()) {
  return (translations[lang] && translations[lang][key]) || key;
}

// Apply a language: set <html> dir/lang, swap fonts, translate all [data-i18n] nodes.
export function applyLang(lang) {
  localStorage.setItem(LANG_KEY, lang);
  const html = document.documentElement;
  html.lang = lang;
  html.dir = lang === 'ar' ? 'rtl' : 'ltr';

  // Translate text content
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    el.textContent = t(el.getAttribute('data-i18n'), lang);
  });
  // Translate placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    el.setAttribute('placeholder', t(el.getAttribute('data-i18n-placeholder'), lang));
  });
  // Reveal cloaked content now that text is ready
  document.querySelectorAll('[data-cloak]').forEach((el) => el.removeAttribute('data-cloak'));

  // Let page modules re-render any JS-built content in the new language
  document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}

export function toggleLang() {
  applyLang(getLang() === 'fr' ? 'ar' : 'fr');
}
